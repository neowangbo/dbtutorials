/*
Packages
A package is a means to organize related functions and procedures together, like creating a library, 
but in PL/SQL jargon the library is called a package.


PL/SQL package has two parts:
-package specification
-package body


Syntax:
CREATE [OR REPLACE] PACKAGE <package_name> AS
-- one or more: constant, cursor, function, procedure, or variable declarations
END <package_name>;
*/





